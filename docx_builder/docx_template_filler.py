def _fill_campaign_detail_tables(doc, campaigns):
    camp_idx = 0
    for tbl in doc.tables:
        hdr_str = ' '.join(c.text.strip() for c in tbl.rows[0].cells).lower() if tbl.rows else ''
        if 'campaign topic' not in hdr_str: continue
        if camp_idx >= len(campaigns):
            # No data for this campaign slot — clear placeholder VALUES, keep LABEL cells
            LABEL_CELLS = {'campaign topic', 'campaign title', 'group targeted',
                           'total members & phished', 'start date', 'end date'}
            for row in tbl.rows:
                for cell in row.cells:
                    ct = cell.text.strip()
                    ct_lower = ct.lower()
                    # Only clear cells that are placeholder values, not structural labels
                    is_label = ct_lower in LABEL_CELLS
                    is_placeholder = (ct.startswith('[') or ct == '[DD/MM/YYYY]'
                                      or (ct.startswith('Campaign ') and not is_label))
                    if is_placeholder and not is_label:
                        ps = cell._tc.findall(qn('w:p'))
                        if ps: _set_para(ps[0], '')
            camp_idx += 1
            continue
        c = campaigns[camp_idx] if camp_idx < len(campaigns) else {}
        camp_idx += 1
        vm = {
            'campaign topic':         _clean(c.get('topic', c.get('name', f'Campaign {camp_idx}'))),
            'group targeted':         _clean(c.get('group', '')),
            'campaign title':         _clean(c.get('name', c.get('topic', f'Campaign {camp_idx}')))[:40],
            'total members & phished': f"{c.get('total_members','—') or '—'} Members / {c.get('phished','0')} Phished",
            'start date':             (_clean(c.get('start_date','')) or _extract_date(c.get('_raw',''), 0)).split('|')[0].strip(),
            'end date':               _clean(c.get('end_date',''))   or _extract_date(c.get('_raw',''), -1),
        }
        for row in tbl.rows:
            for ci in range(0, len(row.cells)-1, 2):
                lbl = row.cells[ci].text.strip().lower()
                for k, v in vm.items():
                    if k in lbl and v:
                        ps = row.cells[ci+1]._tc.findall(qn('w:p'))
                        if ps: _set_para(ps[0], v); break


def _fill_employee_9col(doc, employees):
    """Fill employee interaction table, aligning columns by header names."""
    FIELD_MAP = {
        'employee id':            'id',
        'id':                     'id',
        'name':                   'name',
        'department':             'dept',
        'email':                  'email',
        'email address':          'email',
        'opened':                 'opened',
        'email opened':           'opened',
        'clicked':                'clicked',
        'link clicked':           'clicked',
        'credentials':            'creds',
        'credentials submitted':  'creds',
        'time':                   'time',
        'time to click':          'time',
        'outcome':                'outcome',
    }
    for tbl in doc.tables:
        hdr     = [c.text.strip() for c in tbl.rows[0].cells] if tbl.rows else []
        hdr_str = ' '.join(hdr).lower()
        if 'employee id' not in hdr_str and 'emp-' not in hdr_str: continue
        if 'opened' not in hdr_str and 'clicked' not in hdr_str: continue

        # Map header columns to field names
        col_fields = []
        for h in hdr:
            hl = h.lower().strip()
            field = next((v for k, v in FIELD_MAP.items() if k in hl), None)
            col_fields.append(field)

        rows = []
        for emp in employees:
            row = []
            for field in col_fields:
                if field:
                    row.append(emp.get(field, '—') or '—')
                else:
                    row.append('—')
            rows.append(row)

        if rows:
            _fill_repeat_table(tbl, rows)
            print(f"[FILLER] Filled 9-col employee table: {len(rows)} rows")
        break


def _fill_appendix_table(doc, employees, meta=None):
    """Fill appendix employee table — prefers structured meta.phished_employees."""
    # Try structured data from form first
    structured = []
    phished_raw = (meta or {}).get('phished_employees', '')
    if phished_raw:
        for i, line in enumerate(phished_raw.strip().split('\n')):
            line = line.strip()
            if not line: continue
            if ' | ' in line:
                parts = [p.strip() for p in line.split(' | ')]
            else:
                parts = [line, '']
            email = parts[0] if parts else ''
            dept  = parts[1] if len(parts) > 1 else ''
            if email:
                structured.append({'email': email, 'dept': dept})
        if structured:
            employees = structured

    for tbl in doc.tables:
        hdr = [c.text.strip() for c in tbl.rows[0].cells] if tbl.rows else []
        if '#' in hdr and 'Email' in hdr and 'Department' in hdr and employees:
            _fill_repeat_table(tbl, [[str(i+1), e['email'], e['dept']] for i, e in enumerate(employees)])
            print(f"[FILLER] Filled appendix table: {len(employees)} employees")
            break


def _fill_all_placeholders(doc, meta):
    # Fill body paragraphs
    for para in doc.paragraphs:
        if '{{' in para.text:
            _set_para(para._p, _fill_placeholders(para.text, meta))
    # Fill table cells
    for tbl in doc.tables:
        for row in tbl.rows:
            for cell in row.cells:
                for para in cell.paragraphs:
                    if '{{' in para.text:
                        _set_para(para._p, _fill_placeholders(para.text, meta))
    # Fill headers and footers — {{classification_short}}, {{report_version}}, etc.
    for section in doc.sections:
        for hdr_ftr in [section.header, section.footer,
                        section.first_page_header, section.first_page_footer,
                        section.even_page_header, section.even_page_footer]:
            if hdr_ftr is None:
                continue
            try:
                for para in hdr_ftr.paragraphs:
                    if '{{' in para.text:
                        _set_para(para._p, _fill_placeholders(para.text, meta))
                for tbl in hdr_ftr.tables:
                    for row in tbl.rows:
                        for cell in row.cells:
                            for para in cell.paragraphs:
                                if '{{' in para.text:
                                    _set_para(para._p, _fill_placeholders(para.text, meta))
            except Exception:
                pass


def _fill_ai_markers(doc, sections):
    MARKER    = re.compile(r'^\[AI:([\w]+)\]$')
    BARE      = re.compile(r'^\[([\w]+)\]$')
    INLINE    = re.compile(r'\[AI:([\w]+)\]')
    for para in doc.paragraphs:
        raw = para.text.strip()
        m   = MARKER.match(raw) or BARE.match(raw)
        if m:
            key = m.group(1).lower()
            val = sections.get(key, '')
            if val:
                _set_para(para._p, _clean(val).split('\n')[0].strip()[:500])
                print(f"[FILLER] Filled: [{key}]")
            else:
                _set_para(para._p, '')  # clear unfilled markers — no visible [key] in output
        elif '[AI:' in raw:
            mi = INLINE.search(raw)
            if mi:
                key = mi.group(1).lower()
                val = sections.get(key, '')
                if val:
                    _set_para(para._p, raw.replace(mi.group(0), _clean(val).split('\n')[0].strip()[:200]))
    for tbl in doc.tables:
        for row in tbl.rows:
            for cell in row.cells:
                for para in cell.paragraphs:
                    raw = para.text.strip()
                    if '[AI:' in raw:
                        mi = INLINE.search(raw)
                        if mi:
                            key = mi.group(1).lower()
                            val = sections.get(key, '')
                            if val:
                                _set_para(para._p, _clean(val).split('\n')[0].strip()[:400])


