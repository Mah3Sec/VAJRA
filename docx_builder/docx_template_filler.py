# ── Low-level XML helpers ─────────────────────────────────────────────────────

def _clean(text):
    """Strip markdown from text. Never applied to [AI:key] marker names."""
    t = str(text or '')
    if t.strip().startswith('[') and ']' in t:
        return t.strip()
    t = re.sub(r'\*{1,3}([^*]+)\*{1,3}', r'\1', t)
    t = re.sub(r'_{1,2}([^_]+)_{1,2}',   r'\1', t)
    t = re.sub(r'`([^`]+)`',              r'\1', t)
    return t.strip()


def _set_para(p_elem, text):
    """Replace text in a w:p element, preserving first run's formatting."""
    runs = p_elem.findall(qn('w:r'))
    rpr = None
    if runs:
        rp = runs[0].find(qn('w:rPr'))
        if rp is not None:
            rpr = copy.deepcopy(rp)
        for r in list(runs):
            p_elem.remove(r)
    for hl in list(p_elem.findall(qn('w:hyperlink'))):
        p_elem.remove(hl)
    nr = OxmlElement('w:r')
    if rpr is not None:
        nr.append(rpr)
    t = OxmlElement('w:t')
    t.text = _clean(text)
    if t.text and (t.text[0] == ' ' or t.text[-1] == ' '):
        t.set('{http://www.w3.org/XML/1998/namespace}space', 'preserve')
    nr.append(t)
    p_elem.append(nr)


def _fill_repeat_table(table, data_rows):
    """Replace [REPEAT] template row with actual data rows."""
    tbl_el  = table._tbl
    all_trs = tbl_el.findall(qn('w:tr'))
    if len(all_trs) < 2:
        return
    tmpl_tr = None
    for tr in all_trs[1:]:
        row_text = ''.join(t.text or '' for t in tr.findall('.//' + qn('w:t')))
        if '[REPEAT]' in row_text or not row_text.strip():
            tmpl_tr = tr; break
    if tmpl_tr is None:
        tmpl_tr = all_trs[1]
    for tr in all_trs[1:]:
        tbl_el.remove(tr)
    for row_vals in data_rows:
        new_tr = copy.deepcopy(tmpl_tr)
        tcs    = new_tr.findall(qn('w:tc'))
        for ci, tc in enumerate(tcs):
            if ci >= len(row_vals): break
            for r in list(tc.findall('.//' + qn('w:r'))): r.getparent().remove(r)
            p = tc.find(qn('w:p'))
            if p is None:
                p = OxmlElement('w:p'); tc.append(p)
            nr = OxmlElement('w:r'); t_el = OxmlElement('w:t')
            t_el.text = str(row_vals[ci] or '')
            nr.append(t_el); p.append(nr)
        tbl_el.append(new_tr)


def _extract_date(text, idx=0):
    """Extract the idx-th date from text. Supports MM/DD/YYYY, YYYY-MM-DD etc."""
    dates = re.findall(
        r'\b(?:\d{1,2}[/.\-]\d{1,2}[/.\-]\d{2,4}|\d{4}[/.\-]\d{1,2}[/.\-]\d{1,2})\b', text)
    if not dates: return ''
    return dates[-1] if idx == -1 else (dates[idx] if idx < len(dates) else '')


# ── Placeholder filling ───────────────────────────────────────────────────────

def _fill_placeholders(text, meta):
    """Replace {{token}} with meta values."""
    mapping = {
        'client':          meta.get('client', ''),
        'tester':          meta.get('tester', ''),
        'date':            meta.get('date', ''),
        'engagement_ref':  meta.get('engagement_ref') or meta.get('report_id', ''),
        'classification':  meta.get('classification', 'CONFIDENTIAL'),
        'report_version':  meta.get('report_version', '1.0'),
        'assessment_team': meta.get('assessment_team', meta.get('tester', '')),
        'security_team':   meta.get('assessment_team', meta.get('tester', '')),
        'reviewer':        meta.get('reviewer', ''),
        'approver':        meta.get('approver', ''),
        'target_system':   meta.get('target_system', meta.get('scope', '')),
        'tested_from':     meta.get('tested_from', ''),
        'title':           meta.get('title', ''),
        'scope':           meta.get('scope', ''),
        'prepared_by':            meta.get('tester', ''),
        'classification_short':   (meta.get('classification','CONFIDENTIAL') or 'CONFIDENTIAL').split('\u2014')[0].strip(),
    }
    for k, v in mapping.items():
        text = text.replace('{{' + k + '}}', str(v or ''))
        text = text.replace('{{ ' + k + ' }}', str(v or ''))
    return text


# ── AI section parsing ────────────────────────────────────────────────────────

def parse_ai_sections(ai_text, meta=None):
    """
    Parse AI markdown output into {key: content} dict.
    Derived inline markers (resilience_calc_1, results_stats etc.) are
    synthesised from parsed sections automatically.
    """
    HM = {
        'executive summary':            'executive_summary',
        'results':                      'results_stats',
        'detailed statistics':          'detailed_stats',
        'detailed statistics and':      'detailed_stats',
        'statistics and metrics':       'detailed_stats',
        'overall campaign':             'detailed_stats',
        'individual employee':          'detailed_stats',
        'employee interaction':         'detailed_stats',
        'detailed analysis':            'detailed_analysis',
        'campaign mapped':              'campaign_summary',
        'campaign mapped to':           'campaign_summary',
        'benchmark':                    'benchmark',
        'recommendation':               'recommendation',
        'remediation':                  'remediation',
        'phishing resilience':          'resilience_score',
        'resilience score':             'resilience_score',
        'our calculation':              'our_calculation',
        'interpretation':               'resilience_interpretation',
        'campaign details':             'campaign_details',
        'methodology':                  'methodology',
        'key takeaway':                 'detailed_stats',
        'email pretext':                'email_template_description',
        'observations':                 'detailed_stats',
    }
    for n in range(1, 9):
        HM[f'campaign {n}']            = f'campaign_{n}'
        HM[f'campaign {n}:']           = f'campaign_{n}'
        HM[f'campaign {n} results']    = f'campaign_{n}_stats'
        HM[f'campaign {n} statistics'] = f'campaign_{n}_stats'

    s, ck, cl, in_app = {}, None, [], False

    for line in ai_text.split('\n'):
        st = line.strip()
        is_h = bool(re.match(r'^#{1,4}\s+', st))
        if is_h:
            if ck and cl:
                existing = s.get(ck, '')
                body = '\n'.join(cl).strip()
                s[ck] = (existing + '\n' + body).strip() if existing else body
            ht   = _clean(re.sub(r'^#+\s+', '', st)).lower()
            hlvl = len(st) - len(st.lstrip('#'))
            is_appendix = (hlvl == 1) and 'appendix' in ht
            if is_appendix:
                in_app = True; ck = 'appendix'; cl = []
            elif in_app:
                ck = 'appendix'; cl = [line]   # sub-headings accumulate into appendix
            else:
                in_app = False
                ck = next((v for p, v in HM.items() if p in ht), None)
                if not ck:
                    ck = re.sub(r'[^a-z0-9]', '_', ht)[:25].strip('_') or None
                cl = []
        elif ck:
            cl.append(line)

    if ck and cl:
        existing = s.get(ck, '')
        body = '\n'.join(cl).strip()
        s[ck] = (existing + '\n' + body).strip() if existing else body

    # Derive inline markers
    _derive_meta = meta
    try:
        _derive_markers(s, meta=_derive_meta)
    except Exception as e:
        print(f"[FILLER] Marker derivation warning: {e}")

    return s


def _safe_int(v):
    """Safely convert a value to int, returning 0 on failure."""
    try: return int(str(v).strip().split()[0].replace(',',''))
    except: return 0


def _derive_markers(s, meta=None):
    """Synthesise derived keys (results_stats, resilience_calc_1, etc.)."""
    # Compute resilience score directly from meta numbers if available
    if meta:
        total    = _safe_int(meta.get("total_recipients") or meta.get("emails_sent"))
        clicked  = _safe_int(meta.get("clicked"))
        reported = _safe_int(meta.get("reported"))
        if total and total > 0:
            click_rate   = round(clicked / total * 100, 1) if clicked else 0
            report_rate  = round(reported / total * 100, 1) if reported else 0
            non_click    = round(100 - click_rate, 1)
            resilience   = round((non_click * 0.4) + (report_rate * 0.6), 2)
            s.setdefault("results_stats",
                f"Total: {total} recipients | Phished: {clicked} ({click_rate}%) | "
                f"Reported: {reported} ({report_rate}%) | Resilience Score: {resilience}%")
            s.setdefault("results_metrics",
                f"Click Rate: {click_rate}% | Report Rate: {report_rate}% | "
                f"Non-Click Rate: {non_click}%")
            s.setdefault("resilience_calc_1", f"Non-Click Rate = 100% − {click_rate}% = {non_click}%")
            s.setdefault("resilience_calc_2",
                f"Phishing Resilience Score = [({non_click} × 0.4) + ({report_rate} × 0.6)] = "
                f"{round(non_click*0.4,2)} + {round(report_rate*0.6,2)} = {resilience}")
            s.setdefault("resilience_calc_3", f"Therefore, as percentage: {resilience}%")
    # results_stats
    r = s.get('results', '')
    if r:
        pct = [_clean(l) for l in r.split('\n') if '%' in l and l.strip() and not l.strip().startswith('|')]
        if not pct:
            for l in r.split('\n'):
                if l.strip().startswith('|') and '%' in l and '---' not in l:
                    pts = [_clean(p).strip() for p in l.strip('|').split('|')]
                    if len(pts) >= 2 and '%' in pts[1] and pts[0].lower() not in ('metric', ''):
                        pct.append(f"{pts[0]}: {pts[1]}")
        s.setdefault('results_stats', ' | '.join(pct[:4]) if pct else _clean(r.split('\n')[0])[:200])
        tbl = [l for l in r.split('\n') if l.strip().startswith('|') and '---' not in l]
        ms = [f"{_clean(p[0])}: {_clean(p[1])}"
              for t in tbl[:10]
              for p in [[x.strip() for x in t.strip('|').split('|')]]
              if len(p) >= 2 and p[0] and p[1] and _clean(p[0]).lower() not in ('metric', 'category', '')]
        s.setdefault('results_metrics', ' | '.join(ms[:6]) if ms else s.get('results_stats', '')[:200])

    # recommendation_target — secondary goal/target sentence (distinct from main recommendation)
    rec = s.get('recommendation', '')
    if rec:
        lines_r = [_clean(l) for l in rec.split('\n') if _clean(l) and not _clean(l).startswith('#')]
        # Look for a distinct target/goal sentence
        for ln in lines_r[1:]:  # Skip first line (same as recommendation)
            if any(w in ln.lower() for w in ['71%', 'aim', 'target', 'goal', 'reduce', 'improve', 'achieve']):
                s.setdefault('recommendation_target', ln[:300]); break
        # Fallback: use a generic target if nothing distinct found
        s.setdefault('recommendation_target', 'Aim to achieve a Phishing Resilience Score greater than 71%.')

    # recommendations_text
    src = s.get('remediation', '') or s.get('recommendation', '')
    if src:
        pl = [_clean(l) for l in src.split('\n') if _clean(l) and not _clean(l).startswith('#')]
        s.setdefault('recommendations_text', pl[0][:400] if pl else src[:200])

    # resilience_calc_1/2/3
    calc = (s.get('our_calculation', '') or s.get('resilience_score', '') or
            next((v for v in [s.get('remediation',''), s.get('recommendation','')]
                  if v and ('=' in v or 'non-click' in v.lower() or 'prs' in v.lower())), ''))
    if calc:
        # Skip raw table rows (| col | col |) — they contain % but aren't calc lines
        cl = [_clean(l).lstrip('-*• ')
              for l in calc.split('\n')
              if _clean(l) and not _clean(l).startswith('|')
              and ('=' in l or 'non-click' in l.lower()
                   or 'therefore' in l.lower() or 'prs' in l.lower()
                   or ('rate' in l.lower() and '%' in l and '=' in l))]
        if not cl:
            cl = [_clean(l).lstrip('-*• ') for l in calc.split('\n')
                  if _clean(l) and not _clean(l).startswith('|')]
        if cl:
            s.setdefault('resilience_calc_1', cl[0][:200])
            s.setdefault('resilience_calc_2', cl[min(1, len(cl)-1)][:200])
            s.setdefault('resilience_calc_3', cl[min(2, len(cl)-1)][:200])

    # email_template_description
    for ck in ['campaign_1', 'campaign_details']:
        cs = s.get(ck, '')
        in_template_section = False
        for ln in cs.split('\n'):
            l = _clean(ln).lstrip('-*• ')
            ll = l.lower()
            # Detect "Phishing Email Template:" label — next non-empty line is the description
            if any(x in ll for x in ['phishing email template:', 'email template:']):
                in_template_section = True
                continue
            if in_template_section and l and not l.startswith(('|','#')) and len(l) > 15:
                s.setdefault('email_template_description', l[:300])
                break
            # Also accept lines with pretext/impersonation but NOT starting with 'Campaign'
            if (not in_template_section and len(l) > 20 and not l.startswith(('|','#','Campaign'))
                    and any(w in ll for w in ['pretext','impersonat','subject','template description',
                                              'simulated','lookalike','password expir'])):
                s.setdefault('email_template_description', l[:300]); break
        if 'email_template_description' in s: break
    s.setdefault('email_template_description', 'Phishing email template — see campaign details.')

    # campaign_N_stats — prose only, no raw table rows
    for n in range(1, 9):
        cs = s.get(f'campaign_{n}', '')
        if cs:
            sl = [_clean(l).lstrip('-*• ') for l in cs.split('\n')
                  if _clean(l) and not _clean(l).startswith('|')
                  and any(w in l.lower() for w in
                  ['members','phished','targeted','%','start','end','total','delivered','click'])]
            s.setdefault(f'campaign_{n}_stats', ' | '.join(sl[:3]) if sl else '')

    # detailed_stats — prefer prose lines over raw table markdown
    ds = s.get('detailed_stats', '')
    if ds:
        prose = [_clean(l) for l in ds.split('\n')
                 if _clean(l) and not _clean(l).startswith('|') and not _clean(l).startswith('#')]
        if prose:
            s['detailed_stats'] = prose[0][:400]
        else:
            # Section is all tables — write a brief summary sentence
            s['detailed_stats'] = 'Detailed employee interaction data is presented in the table below.'
        # Override — if it's still a raw table row, replace
        if s.get('detailed_stats','').startswith('|'):
            s['detailed_stats'] = 'Detailed employee interaction data is presented in the table below.'


