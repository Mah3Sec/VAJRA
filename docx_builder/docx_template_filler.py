def _extract_campaigns(sections, meta=None):
    """Extract campaign data — prefers structured meta.campaigns over AI text parsing."""

    # Priority 1: Structured campaign data passed directly from the form
    if meta and meta.get("campaigns"):
        campaigns = []
        for i, c in enumerate(meta["campaigns"]):
            if not c: continue
            campaigns.append({
                'num':           i + 1,
                'name':          c.get('title', '') or c.get('topic', '') or f'Campaign {i+1}',
                'topic':         c.get('topic', '') or c.get('title', '') or f'Campaign {i+1}',
                'group':         c.get('group', ''),
                'total_members': str(c.get('total', '') or ''),
                'phished':       str(c.get('phished', '') or '0'),
                'start_date':    c.get('start', '') or '',
                'end_date':      c.get('end', '') or '',
                'pretext':       c.get('pretext', '') or '',
                '_raw':          '',
            })
        if campaigns:
            print(f"[FILLER] Using {len(campaigns)} structured campaigns from form data")
            return campaigns

    campaigns = []

    # First priority: campaign_summary TABLE rows from AI output
    # These have the most complete data for all campaigns
    summary_text = sections.get('campaign_summary', '')
    for line in summary_text.split('\n'):
        if line.strip().startswith('|') and '---' not in line:
            pts = [_clean(p).strip() for p in line.strip('|').split('|')]
            if len(pts) >= 4 and pts[0] and re.match(r'^\d+$', pts[0]):
                campaigns.append({
                    'num':           int(pts[0]),
                    'name':          pts[1],
                    'topic':         pts[1],
                    'group':         pts[2],
                    'total_members': pts[3].replace(' members', ''),
                    'phished':       pts[4] if len(pts) > 4 else '0',
                    'start_date':    '', 'end_date':    '', '_raw': '',
                })

    # Enrich with details from campaign_N sections (start/end dates, better title)
    for c in campaigns:
        cs = sections.get(f"campaign_{c['num']}", '')
        if not cs: continue
        c['_raw'] = cs
        for line in cs.split('\n'):
            l = re.sub(r'\*+', '', _clean(line)).lstrip('-• ').strip()
            ll = l.lower()
            for fld, pats in [
                ('start_date', ['start date:', 'start:', 'launched:','campaign start:']),
                ('end_date',   ['end date:', 'end:', 'closed:','campaign end:']),
                ('topic',      ['campaign name:', 'campaign theme:', 'pretext:','email pretext:']),
                ('group',      ['group targeted:','target group:','department:']),
            ]:
                for pat in pats:
                    if ll.startswith(pat) and not c.get(fld):
                        c[fld] = l[len(pat):].strip()[:80]; break
        if not c['start_date']: c['start_date'] = _extract_date(cs, 0)
        if not c['end_date']:   c['end_date']   = _extract_date(cs, -1)

    if campaigns:
        return campaigns

    # Fallback: extract from campaign_N sections directly
    for n in range(1, 9):
        cs = sections.get(f'campaign_{n}', '')
        if not cs: continue
        cd = {'num':n,'name':f'Campaign {n}','topic':'','group':'',
              'total_members':'','phished':'0','start_date':'','end_date':'','_raw':cs}
        for line in cs.split('\n'):
            # Handle both plain "Key: val" and bold "**Key:** val" markdown
            l = re.sub(r'\*+', '', _clean(line)).lstrip('-• ').strip()
            ll = l.lower()
            for fld, pats in [
                ('topic',         ['campaign name:','campaign theme:','pretext:','email pretext:','name:','subject line:']),
                ('group',         ['group targeted:','target group:','target population:','department:','targeted group:']),
                ('total_members', ['total targeted:','total members:','total employees:','total recipients:']),
                ('phished',       ['phished:','failed:','credentials submitted:','clicked:','link clicked:']),
                ('start_date',    ['start date:','start:','launched:','campaign start:']),
                ('end_date',      ['end date:','end:','closed:','campaign end:']),
            ]:
                for pat in pats:
                    if ll.startswith(pat):
                        val = l[len(pat):].strip()
                        # Extract just the number/value before any % or extra text
                        if fld in ('total_members','phished'):
                            m = re.match(r'^(\d+)', val)
                            if m: val = m.group(0)
                        cd[fld] = val[:80]; break
        if not cd['topic']:
            for line in cs.split('\n'):
                l = _clean(line).lstrip('-*# ')
                if re.match(r'^Campaign \d+:?\s*$', l, re.I): continue
                if l and len(l) > 5 and not l.startswith(('|','#')): cd['topic'] = l[:80]; break
        if not cd['start_date']: cd['start_date'] = _extract_date(cs, 0)
        if not cd['end_date']:   cd['end_date']   = _extract_date(cs, -1)
        campaigns.append(cd)

    if not campaigns:
        for line in sections.get('campaign_summary', '').split('\n'):
            if line.strip().startswith('|') and '---' not in line:
                pts = [_clean(p).strip() for p in line.strip('|').split('|')]
                if len(pts) >= 4 and pts[0] and re.match(r'^\d+$', pts[0]):
                    campaigns.append({'num':int(pts[0]),'name':pts[1],'topic':pts[1],
                                      'group':pts[2],'total_members':pts[3].replace(' members',''),
                                      'phished':pts[4] if len(pts)>4 else '0',
                                      'start_date':'','end_date':'','_raw':''})

    # For campaigns with no group, try to get from campaign_N section
    for c in campaigns:
        if not c.get('group'):
            cs = sections.get(f"campaign_{c['num']}", '')
            for line in cs.split('\n'):
                l = _clean(line).lstrip('-*• '); ll = l.lower()
                for pat in ['group targeted:','target group:','target population:','department:']:
                    if ll.startswith(pat):
                        c['group'] = l[len(pat):].strip()[:80]; break
                if c.get('group'): break

    return campaigns


def _extract_employees(sections):
    employees = []; seen = set()
    for key in ['appendix', 'detailed_stats', 'detailed_analysis']:
        for line in sections.get(key, '').split('\n'):
            if not line.strip().startswith('|') or '---' in line: continue
            pts = [_clean(p).strip() for p in line.strip('|').split('|')]
            if not pts: continue
            if pts[0].lower() in ('employee id','#','no','') or 'name' in pts[0].lower(): continue
            if not (re.match(r'^EMP-', pts[0], re.I) or re.match(r'^\d+$', pts[0])): continue
            if pts[0] in seen: continue
            seen.add(pts[0])

            # Find email column — search ALL columns for @
            email_col = next((i for i, p in enumerate(pts) if '@' in p), -1)
            email = pts[email_col] if email_col >= 0 else ''

            # Detect layout: EMP-ID | Name | Dept | Email | Opened | Clicked | Creds | Time | Outcome
            if email_col == 3 and len(pts) >= 4:
                employees.append({
                    'id':pts[0], 'name':pts[1], 'dept':pts[2], 'email':pts[3],
                    'opened':pts[4] if len(pts)>4 else '', 'clicked':pts[5] if len(pts)>5 else '',
                    'creds':pts[6] if len(pts)>6 else '', 'time':pts[7] if len(pts)>7 else '',
                    'outcome':pts[8] if len(pts)>8 else '', '_all':pts})
            elif email_col == 1 and len(pts) >= 3:
                # # | Email | Dept
                employees.append({'id':pts[0],'name':'','dept':pts[2] if len(pts)>2 else '',
                                   'email':pts[1],'opened':'','clicked':'','creds':'',
                                   'time':'','outcome':'','_all':pts})
            else:
                # Flexible mapping: try to identify columns by content
                # Common AI output: EMP-ID | Name | Dept | Email | Opened | Clicked | Creds | Time | Outcome
                # or shorter:       EMP-ID | Name | Dept | Email | Time | Outcome
                opened  = next((p for p in pts[4:] if p.lower() in ('yes','no')), '')
                clicked = next((p for i,p in enumerate(pts[4:]) if p.lower() in ('yes','no') and i > 0), '')
                creds   = next((p for i,p in enumerate(pts[4:]) if p.lower() in ('yes','no') and i > 1), '')
                time_v  = next((p for p in pts if 'min' in p.lower() or 'n/a' in p.lower()), '')
                outcome = next((p for p in reversed(pts) if any(w in p.lower()
                               for w in ['failed','clicked only','passed','no interaction'])), pts[-1] if pts else '')
                employees.append({'id':pts[0], 'name':pts[1] if len(pts)>1 else '',
                                   'dept':pts[2] if len(pts)>2 else '',
                                   'email':email, 'opened':opened, 'clicked':clicked,
                                   'creds':creds, 'time':time_v, 'outcome':outcome, '_all':pts})
    return employees


