def _safe_logo_path(logo_path):
    """Convert any image to a temporary PNG so add_picture always works."""
    if not logo_path:
        print("[FILLER] Logo: no path provided")
        return None
    if not os.path.exists(logo_path):
        print(f"[FILLER] Logo: file not found at: {logo_path}")
        return None
    print(f"[FILLER] Logo: found {os.path.getsize(logo_path)} bytes at {logo_path}")
    try:
        from PIL import Image
        import tempfile
        img = Image.open(logo_path).convert("RGBA")
        # Composite onto white background (handles transparency)
        bg = Image.new("RGB", img.size, (255, 255, 255))
        bg.paste(img, mask=img.split()[3] if img.mode == "RGBA" else None)
        tmp = tempfile.NamedTemporaryFile(suffix=".png", delete=False)
        bg.save(tmp.name, "PNG")
        tmp.close()
        return tmp.name
    except ImportError:
        return logo_path  # PIL not available — try original path directly
    except Exception as e:
        print(f"[FILLER] Logo convert warning: {e}")
        return logo_path  # fallback to original


def _fill_logo_placeholders(doc, meta):
    """Replace cover logo placeholders with uploaded logos or hint text."""
    comp_logo = meta.get("_company_logo")
    test_logo = meta.get("_tester_logo")
    _tmp_files = []

    for para in doc.paragraphs:
        txt = para.text.strip()

        if "{{company_logo_placeholder}}" in txt:
            for run in list(para.runs):
                run._element.getparent().remove(run._element)
            para.paragraph_format.alignment = 1
            safe_path = _safe_logo_path(comp_logo)
            if safe_path:
                if safe_path != comp_logo:
                    _tmp_files.append(safe_path)
                try:
                    para.add_run().add_picture(safe_path, width=Inches(3.5))
                    print(f"[FILLER] ✓ Company logo placed on cover ({safe_path})")
                except Exception as e:
                    print(f"[FILLER] Company logo error: {type(e).__name__}: {e}")
                    # Try raw path as last resort
                    try:
                        para.add_run().add_picture(comp_logo, width=Inches(3.5))
                        print(f"[FILLER] ✓ Company logo placed (raw path fallback)")
                    except Exception as e2:
                        print(f"[FILLER] Company logo final error: {e2}")
                        _run_plain(para, "[ Client Logo ]", color="1C3F6E", size=14)
            else:
                _run_plain(para, "[ Client Logo — upload via Logos tab ]", color="6B8CAE", size=11, italic=True)

        elif "{{tester_logo_placeholder}}" in txt:
            for run in list(para.runs):
                run._element.getparent().remove(run._element)
            para.paragraph_format.alignment = 1
            safe_path = _safe_logo_path(test_logo)
            if safe_path:
                if safe_path != test_logo:
                    _tmp_files.append(safe_path)
                try:
                    para.add_run().add_picture(safe_path, width=Inches(2.0))
                    print(f"[FILLER] ✓ Assessor logo placed on cover ({safe_path})")
                except Exception as e:
                    print(f"[FILLER] Assessor logo error: {type(e).__name__}: {e}")
                    try:
                        para.add_run().add_picture(test_logo, width=Inches(2.0))
                        print(f"[FILLER] ✓ Assessor logo placed (raw path fallback)")
                    except Exception as e2:
                        print(f"[FILLER] Assessor logo final error: {e2}")
                        _run_plain(para, "[ Assessor Logo ]", color="6B8CAE", size=11)
            else:
                _run_plain(para, "[ Assessor Logo — upload via Logos tab ]", color="6B8CAE", size=11, italic=True)

    # Clean up temp PNG files
    for tmp in _tmp_files:
        try: os.unlink(tmp)
        except: pass


def _fill_doc_info(doc, meta):
    LABELS = {'document title','version number','date of issue','status',
              'classification','performed by','author','reviewer','approver',
              'department','document type'}
    VM = {'document title': f"Phishing Simulation Report \u2014 {meta.get('client','')}",
          'version number': meta.get('report_version','1.0'),
          'date of issue':  meta.get('date',''),
          'status':         'Draft',
          'classification': meta.get('classification','CONFIDENTIAL'),
          'performed by':   meta.get('tester',''),
          'author':         meta.get('author', meta.get('tester','')),
          'reviewer':       meta.get('reviewer',''),
          'approver':       meta.get('approver',''),
          'department':     meta.get('assessment_team',''),
          'document type':  'Technical Report'}
    for tbl in doc.tables:
        if not tbl.rows or len(tbl.rows[0].cells) != 2: continue
        if tbl.rows[0].cells[0].text.strip().lower() not in LABELS: continue
        for row in tbl.rows:
            if len(row.cells) < 2: continue
            lbl = row.cells[0].text.strip().lower()
            for k, v in VM.items():
                if k in lbl and v:
                    ps = row.cells[1]._tc.findall(qn('w:p'))
                    if ps: _set_para(ps[0], v); break


