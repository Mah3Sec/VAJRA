def _fill_header_footer_xml(output_path, meta):
    """
    Fill {{placeholders}} in header/footer XML files directly.
    Required because headers/footers often contain text boxes (wps:txbx)
    that python-docx's section.header/footer API cannot access.
    """
    import zipfile, shutil, tempfile

    mapping = {
        '{{classification_short}}': (meta.get('classification','CONFIDENTIAL') or 'CONFIDENTIAL').split('—')[0].strip(),
        '{{report_version}}':       meta.get('report_version', '1.0') or '1.0',
        '{{client}}':               meta.get('client', '') or '',
        '{{tester}}':               meta.get('tester', '') or '',
        '{{date}}':                 meta.get('date', '') or '',
        '{{assessment_team}}':      meta.get('assessment_team', '') or '',
        '{{classification}}':       meta.get('classification', 'CONFIDENTIAL') or 'CONFIDENTIAL',
    }

    # Read zip, edit, write back
    tmp = output_path + '.hdrtmp'
    try:
        with zipfile.ZipFile(output_path, 'r') as zin:
            file_data = {n: zin.read(n) for n in zin.namelist()}

        changed = False
        for name in list(file_data.keys()):
            if not (name.startswith('word/header') or name.startswith('word/footer')):
                continue
            if not name.endswith('.xml'):
                continue
            xml = file_data[name].decode('utf-8')
            original = xml
            for placeholder, value in mapping.items():
                xml = xml.replace(placeholder, str(value))
            if xml != original:
                file_data[name] = xml.encode('utf-8')
                changed = True
                print(f"[FILLER] Filled header/footer placeholders in {name}")

        if changed:
            with zipfile.ZipFile(tmp, 'w', zipfile.ZIP_DEFLATED) as zout:
                for name, data in file_data.items():
                    zout.writestr(name, data)
            shutil.move(tmp, output_path)
    except Exception as e:
        print(f"[FILLER] Header/footer XML fill warning: {e}")
        if os.path.exists(tmp):
            os.unlink(tmp)


def fill_docx_template(template_path, output_path, meta, ai_content_text):
    """
    Fill a .docx template with engagement metadata and AI content.
    Cover page is NOT added here — app.py merges the VAJRA cover separately.

    Returns True on success, False on failure.
    """
    try:
        print(f"[FILLER] Loading template: {template_path}")
        shutil.copy2(template_path, output_path)
        doc = Document(output_path)

        # 1. Parse AI sections
        sections = parse_ai_sections(ai_content_text, meta=meta)
        print(f"[FILLER] Sections: {[k for k in sections if not k.startswith('_')][:14]}")

        # 2. Fill logos first — must see raw {{logo_placeholder}} before _fill_all_placeholders wipes them
        _fill_logo_placeholders(doc, meta)

        # 3. Fill {{placeholders}}
        _fill_all_placeholders(doc, meta)

        # 4. Fill [AI:marker] paragraphs and table cells
        _fill_ai_markers(doc, sections)

        # 5. Fill structured metadata tables
        _fill_doc_info(doc, meta)
        _fill_version_history(doc, meta)
        _fill_distribution_list(doc, meta)
        _fill_recommendations_table(doc, meta, sections)

        # 5. Fill campaign tables
        campaigns = _extract_campaigns(sections, meta=meta)
        print(f"[FILLER] Campaigns: {len(campaigns)}")
        _fill_campaign_summary_table(doc, campaigns)
        _fill_campaign_detail_tables(doc, campaigns)

        # 6. Fill employee tables
        employees = _extract_employees(sections)
        print(f"[FILLER] Employees: {len(employees)}")
        _fill_employee_9col(doc, employees)
        _fill_appendix_table(doc, employees, meta=meta)

        doc.save(output_path)
        # Fill header/footer text boxes that python-docx API can't reach
        _fill_header_footer_xml(output_path, meta)
        print(f"[FILLER] Saved: {output_path} ({os.path.getsize(output_path):,} bytes)")

        # NOTE: No VAJRA cover prepended for .docx templates.
        # The custom .docx template already contains its own cover page
        # with {{placeholders}} filled above. Adding another cover would duplicate it.
        # VAJRA cover is only used for .md template outputs (generate.js path).
        print("[FILLER] ✓ Custom .docx template filled — using template's own cover page")
        return True

    except Exception as e:
        import traceback
        print(f"[FILLER] Error: {e}")
        traceback.print_exc()
        return False
